    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        errorLabel.setText("");
        errorLabel.setForeground(Color.red);
        boolean flag = true;
        if(usernameField.getText().trim().equals("")){flag = false;}
        if(String.valueOf(passwordField.getPassword()).trim().equals("")){flag = false;}
        if(String.valueOf(retypePassField.getPassword()).trim().equals("")){flag = false;}
        if(gmailField.getText().trim().equals("")){flag = false;}
        String username = usernameField.getText().trim();
        String password = String.valueOf(passwordField.getPassword()).trim();
        String gmail = gmailField.getText().trim();
        
        if(flag == false){errorLabel.setText("You must fill all fields ."); return;}
        
        if(!password.equals(String.valueOf(retypePassField.getPassword()).trim())){
            errorLabel.setText("please .. retype password correctly .");
            return;
        }
        if(!gmail.endsWith("@gmail.com")){
            errorLabel.setText("please .. enter correct gmail");
            return;
        }
        
        PreparedStatement stmt = null;
        Connection con = null;
        flag = false;
        try{
            DriverManager.registerDriver(new oracle.jdbc.driver.OracleDriver());
            con = DriverManager.getConnection(LibraryManagementSystem.URL);
            flag = true;
            stmt = con.prepareStatement("insert into users (id, username, password, email, disabled) values (users_counter.nextval, ?, ?, ?, 'abled')");
            stmt.setString(1, username);
            stmt.setString(2, password);
            stmt.setString(3, gmail);
            stmt.executeUpdate();
            stmt.close();
            con.close();
        }
        catch(SQLException e){
            errorLabel.setText(flag? "this username is used, change it ." : "failed in connection to the database .");
            return;
        }
        
        errorLabel.setForeground(Color.green);
        errorLabel.setText("signed up successfully .");
        usernameField.setText("");
        passwordField.setText("");
        retypePassField.setText("");
        gmailField.setText("");                                       

    }//GEN-LAST:event_jButton8ActionPerformed

