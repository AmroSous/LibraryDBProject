    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(JOptionPane.showConfirmDialog(this, "Be careful, press yes if you want to delete all data of updates .", "warning", JOptionPane.YES_NO_OPTION) == 1){
            return;
        }else{
            try{
                DriverManager.registerDriver(new oracle.jdbc.driver.OracleDriver());
                Connection con = DriverManager.getConnection(LibraryManagementSystem.URL);
                Statement s = con.createStatement();
                ResultSet r = s.executeQuery("delete from user_moves");
                Functions.buildTableModel(r, jTable3);
                s.close();
                con.close();
                findUpdates();
            }catch(SQLException e){
                JOptionPane.showMessageDialog(null, "Failed in connection .", "error", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
                return;
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

