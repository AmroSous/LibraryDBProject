    private void jLabel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel3MouseClicked
        ResultSet rs = null;
        PreparedStatement stmt = null;
        Connection con = null;
        try {
            int id = Integer.parseInt(bookIdField.getText().toString());
            int quantity = Integer.parseInt(quantityField.getText().toString());
            if (quantity < 0) {
                throw new NumberFormatException();
            }
            if (!ids.contains(id)) {
                JOptionPane.showMessageDialog(null, "Not found .", "warning", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            if (this.quantity.get(ids.indexOf(id)) < quantity) {
                JOptionPane.showMessageDialog(null, "you have not this quantity.", "warning", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            DriverManager.registerDriver(new oracle.jdbc.driver.OracleDriver());
            con = DriverManager.getConnection(LibraryManagementSystem.URL);
            stmt = con.prepareStatement("update issue set quantity = quantity - ? where user_id = ? and book_id = ?");
            stmt.setInt(1, quantity);
            stmt.setInt(2, UserInterface.user_id);
            stmt.setInt(3, id);
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Returned successfully", "Done", JOptionPane.INFORMATION_MESSAGE);
            stmt = con.prepareStatement("select issue.book_id, books.name, issue.issue_date, "
                    + "issue.quantity from issue, books where user_id = ? and books.id = issue.book_id");
            stmt.setInt(1, UserInterface.user_id);
            rs = stmt.executeQuery();
            ids.clear();
            this.quantity.clear();
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            if (rs == null) {
                dtm.setRowCount(0);
                return;
            }
            ResultSetMetaData metaData = rs.getMetaData();

            int columnCount = metaData.getColumnCount();
            int num = 1;

            dtm.setRowCount(0);

            while (rs.next()) {
                ids.add(rs.getInt(1));
                this.quantity.add(rs.getInt("quantity"));
                Vector<Object> vector = new Vector<Object>();
                vector.add(num);
                for (int columnIndex = 1; columnIndex <= columnCount; columnIndex++) {
                    vector.add(rs.getObject(columnIndex));
                }
                num++;
                dtm.addRow(vector);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Failed in connection to the database .", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid input .", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_jLabel3MouseClicked


