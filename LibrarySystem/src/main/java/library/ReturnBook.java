    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        bookIdField.setText("");
        quantityField.setText("");
        searchField.setText("");
        try {
            DriverManager.registerDriver(new oracle.jdbc.driver.OracleDriver());
            Connection con = DriverManager.getConnection(LibraryManagementSystem.URL);
            PreparedStatement stmt = con.prepareStatement("select issue.book_id, books.name, issue.issue_date, "
                    + "issue.quantity from issue, books where user_id = ? and books.id = issue.book_id");
            stmt.setInt(1, UserInterface.user_id);
            ResultSet rs = stmt.executeQuery();
            ids.clear();
            this.quantity.clear();
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            if (rs == null) {
                dtm.setRowCount(0);
                return;
            }
            ResultSetMetaData metaData = rs.getMetaData();

            int columnCount = metaData.getColumnCount();
            int num = 1;

            dtm.setRowCount(0);

            while (rs.next()) {
                ids.add(rs.getInt(1));
                this.quantity.add(rs.getInt("quantity"));
                Vector<Object> vector = new Vector<Object>();
                vector.add(num);
                for (int columnIndex = 1; columnIndex <= columnCount; columnIndex++) {
                    vector.add(rs.getObject(columnIndex));
                }
                num++;
                dtm.addRow(vector);
            }
        } catch (SQLException ex) {
            Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

