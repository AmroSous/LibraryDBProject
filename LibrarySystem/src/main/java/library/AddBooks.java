    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        messageLabel.setForeground(Color.red);
        messageLabel.setText("");
        
        if(bookIdCombo.getSelectedItem().toString().equals("")){
            messageLabel.setText("you must choose the book id .");
            return;
        }
        int id = Integer.parseInt(bookIdCombo.getSelectedItem().toString());
        int index = booksQuantities.indexOf(id);
        if(addQuantityField.getText().trim().equals("")){
            messageLabel.setText("you must fill quantity field .");
            return;
        }
        int quantity;
        PreparedStatement stmt = null;
        Connection con = null;
        ResultSet rs = null;
        try{
            quantity = Integer.parseInt(addQuantityField.getText().trim());
            DriverManager.registerDriver(new oracle.jdbc.driver.OracleDriver());
            con = DriverManager.getConnection(LibraryManagementSystem.URL);
            stmt = con.prepareStatement("update books set quantity = ? where id = ?");
            int newQuantity = quantity + booksQuantities.get(index+1);
            stmt.setInt(1, newQuantity);
            stmt.setInt(2, id);
            stmt.executeUpdate();
            booksQuantities.remove(index+1);
            booksQuantities.add(index+1, newQuantity);
            stmt.close();
            con.close();
            messageLabel.setForeground(Color.green);
            messageLabel.setText("the quantity added successfully .");
        }catch(SQLException e){
            messageLabel.setText("Failed in connection to the database .");
            e.printStackTrace();
        }catch(NumberFormatException e){
            messageLabel.setText("Invalid quantity .");
            return;
        }
        bookIdCombo.setSelectedIndex(0);
        addQuantityField.setText("");
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        try {
            DriverManager.registerDriver(new oracle.jdbc.driver.OracleDriver());
            Connection con = DriverManager.getConnection(LibraryManagementSystem.URL);
            InputStream input = new FileInputStream(new File("IssuedBooks.jrxml"));
            JasperDesign jd = JRXmlLoader.load(input);

            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("select sum(issue.quantity) from issue");
            int sumOfIssuedBooks = 0, sumOfAvailableBooks = 0;
            if (rs.next()) {
                sumOfIssuedBooks = rs.getInt(1);
            }
            rs = stmt.executeQuery("select sum(books.quantity) from books");
            if (rs.next()) {
                sumOfAvailableBooks = rs.getInt(1);
            }

            HashMap<String, Object> para = new HashMap<>();
            para.put("TotalAvaBooks", sumOfAvailableBooks);
            para.put("TotalIssBooks", sumOfIssuedBooks);

            JasperReport jr = JasperCompileManager.compileReport(jd);
            JasperPrint jp = JasperFillManager.fillReport(jr, para, con);

            JFrame frame = new JFrame("Report");
            frame.getContentPane().add(new JRViewer(jp));

            frame.pack();
            Dimension dim = Toolkit.getDefaultToolkit().getScreenSize();
            frame.setLocation(dim.width/2-this.getSize().width/2, dim.height/2-this.getSize().height/2);
            frame.setVisible(true);
            input.close();
            stmt.close();
            con.close();

//            OutputStream os = new FileOutputStream(new File("testReport.pdf"));
//            JasperExportManager.exportReportToPdfStream(jp, os);
//            os.close();
//            input.close();
        } catch (SQLException e) {
            e.printStackTrace();
        } catch (FileNotFoundException ex) {
            ex.printStackTrace();
        } catch (JRException ex) {
            ex.printStackTrace();
        } catch (IOException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton6ActionPerformed

