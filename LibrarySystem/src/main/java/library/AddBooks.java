    private void getBooksIds() {
        bookIdCombo.removeAllItems();
        booksQuantities.clear();
        try {
            DriverManager.registerDriver(new oracle.jdbc.OracleDriver());
            Connection con = DriverManager.getConnection(LibraryManagementSystem.URL);
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("select id, quantity from books");
            booksQuantities.add(-1);
            bookIdCombo.addItem("");
            while (rs.next()) {
                booksQuantities.add(rs.getInt(2));
                bookIdCombo.addItem(rs.getString(1));
            }

        } catch (SQLException e) {
            messageLabel.setText("Failed in connection to the database .");
            e.printStackTrace();
        }
    }
