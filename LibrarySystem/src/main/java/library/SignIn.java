    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        errorLabel.setText("");
        errorLabel.setForeground(Color.red);
        String username = usernameField.getText().trim();
        String password = String.valueOf(passwordField.getPassword()).trim();
        
        if(username.equals("")) {errorLabel.setText("you must enter the username"); return;}
        if(password.equals("")) {errorLabel.setText("you must enter the password"); return;}
        
        try {
            DriverManager.registerDriver(new oracle.jdbc.driver.OracleDriver());
            Connection con = DriverManager.getConnection(LibraryManagementSystem.URL);
            PreparedStatement stmt = con.prepareStatement("select id, username, password, disabled from users where username = ? and password = ?");
            stmt.setString(1, username);
            stmt.setString(2, password);
            ResultSet rs = stmt.executeQuery();
                if(rs.next()){
                    if((rs.getString("disabled")).equals("disabled")){
                        errorLabel.setText("Sorry .. Your account was disabled :( ");
                        usernameField.setText("");
                        passwordField.setText("");
                        return;
                    }
                    ((JFrame)(this.getParent().getParent().getParent().getParent().getParent().getParent())).dispose();
                    new UserInterface(username, rs.getInt("id")).setVisible(true);
                }
                else{
                    stmt = con.prepareStatement("select name, password from admin where name = ? and password = ?");
                    stmt.setString(1, username);
                    stmt.setString(2, password);
                    rs = stmt.executeQuery();
                    if(rs.next()){
                        ((JFrame)(this.getParent().getParent().getParent().getParent().getParent().getParent())).dispose();
                        new AdminInterface(username).setVisible(true);
                    }
                    else{
                        errorLabel.setText("username or password is incorrect .");
                        return;
                    }
                }
            } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Failed in connection to the database .", "Oops!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
            return;
        }
    }//GEN-LAST:event_jButton3ActionPerformed

