    public static String getPasswordOf(String username, String table){
        try {
            DriverManager.registerDriver(new oracle.jdbc.driver.OracleDriver());
            Connection con = DriverManager.getConnection(LibraryManagementSystem.URL);
            PreparedStatement stmt = null;
            if(table.equals("users")){
            stmt = con.prepareStatement("select password from users where username = ?");
            }else if(table.equals("admin")){
                stmt = con.prepareStatement("select password from admin where name = ?");
            }else return null;
            stmt.setString(1, username);
            ResultSet rs = stmt.executeQuery();
                if(rs.next()){
                    return rs.getString("password");
                }
        }catch(SQLException e){
            e.printStackTrace();
        }
        return null;
    }
    
