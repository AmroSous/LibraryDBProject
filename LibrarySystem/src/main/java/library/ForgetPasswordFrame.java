    public static boolean sendMessage(String email, String subject, String text) throws MessagingException{
 
    Session newSession = null;
    MimeMessage mimeMessage = null;

//    setupServerProperties
        Properties properties = System.getProperties();
        properties.put("mail.smtp.port", "587");
        properties.put("mail.smtp.auth", "true");
        properties.put("mail.smtp.starttls.enable", "true");
        newSession = Session.getDefaultInstance(properties, null);


//    draftEmail() 
        String toEmail = email;         ///// here is the receipient Email
        String emailSubject = subject;            ///// here is the subject of email
        String emailText = text;   //// here is the text of email
        
        try{
            mimeMessage = new MimeMessage(newSession);
            mimeMessage.addRecipient(Message.RecipientType.TO , new InternetAddress(toEmail));
            mimeMessage.setSubject(emailSubject);
            mimeMessage.setText(emailText);
        }
        catch(AddressException e){return false;}
        catch(MessagingException e){return false;}

//    sendEmail()

        String fromUser = "librarydb12";
        String fromUserPassword = "ourlibrary";
        String emailHost = "smtp.gmail.com";
        try{
            Transport transport = newSession.getTransport("smtp");
            transport.connect(emailHost, fromUser, fromUserPassword);
            transport.sendMessage(mimeMessage, mimeMessage.getAllRecipients());
            transport.close();
        }
        catch(MessagingException e){return false;}
        return true;

    }

