    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        userIdLabel.setText(String.valueOf(ManageUsers.id));
        usernameLabel.setText(ManageUsers.username);
        emailLabel.setText(ManageUsers.email);
        disabledLabel.setText(ManageUsers.disabled);

        try {
            DriverManager.registerDriver(new oracle.jdbc.driver.OracleDriver());
            Connection con = DriverManager.getConnection(LibraryManagementSystem.URL);
            PreparedStatement stmt = con.prepareStatement("select issue.book_id, books.name, issue.quantity, "
                    + "issue.issue_date from issue, books where user_id = ? and books.id = issue.book_id");
            stmt.setInt(1, ManageUsers.id);
            ResultSet rs = stmt.executeQuery();
            Functions.buildTableModel(rs, jTable3);

        } catch (SQLException ex) {
            Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formComponentShown

