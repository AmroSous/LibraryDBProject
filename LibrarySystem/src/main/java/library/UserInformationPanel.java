    private void setDisabled(String value) throws SQLException {
        DriverManager.registerDriver(new oracle.jdbc.OracleDriver());
        Connection con = DriverManager.getConnection(LibraryManagementSystem.URL);
        PreparedStatement stmt = con.prepareStatement("update users set disabled = ? where id = ?");
        stmt.setString(1, value);
        stmt.setInt(2, ManageUsers.id);
        stmt.executeUpdate();
        stmt.close();
        con.close();
    }

