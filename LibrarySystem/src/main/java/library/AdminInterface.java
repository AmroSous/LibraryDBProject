    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        messageLabel.setText("");
        messageLabel.setForeground(Color.red);
        String pass = String.valueOf(oldPasswordField.getPassword()).trim();
        String password = SignIn.getPasswordOf(adminName, "admin");
        if(!pass.equals(password)){
            messageLabel.setText("Wrong password");
            return;
        }
        String newPass = String.valueOf(newPasswordField.getPassword()).trim();
        String retypePass = String.valueOf(retypePasswordField.getPassword()).trim();
        if(newPass.equals("") || retypePass.equals("")){
            messageLabel.setText("You must fill all fields .");
            return;
        }
        if(!newPass.equals(retypePass)){
            messageLabel.setText("please retype new password correctly .");
            return;
        }
        try{
            DriverManager.registerDriver(new oracle.jdbc.driver.OracleDriver());
            Connection con = DriverManager.getConnection(LibraryManagementSystem.URL);
            PreparedStatement stmt = con.prepareStatement("update admin set password = ? where name = ?");
            stmt.setString(1, newPass);
            stmt.setString(2, adminName);
            stmt.executeUpdate();
            messageLabel.setForeground(Color.green);
            messageLabel.setText("Your password updated successfully .");
            newPasswordField.setText("");
            retypePasswordField.setText("");
            oldPasswordField.setText("");
            return;
            
        }catch(SQLException e){
            messageLabel.setText("Failed in connection to the database .");
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

