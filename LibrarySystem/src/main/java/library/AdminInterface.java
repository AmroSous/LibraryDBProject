    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        messageLabel.setText("");
        messageLabel.setForeground(Color.red);
        String pass = String.valueOf(oldPasswordField.getPassword()).trim();
        String password = SignIn.getPasswordOf(adminName, "admin");
        if(!pass.equals(password)){
            messageLabel.setText("Wrong password");
            return;
        }
        String gmail = gmailField.getText().trim();
        if(gmail.equals("")){
            messageLabel.setText("You must type your gmail .");
            return;
        }
        if(!gmail.endsWith("@gmail.com")){
            messageLabel.setText("you must enter correct gmail .");
            return;
        }
        try{
            DriverManager.registerDriver(new oracle.jdbc.driver.OracleDriver());
            Connection con = DriverManager.getConnection(LibraryManagementSystem.URL);
            PreparedStatement stmt = con.prepareStatement("update admin set email = ? where name = ?");
            stmt.setString(1, gmail);
            stmt.setString(2, adminName);
            stmt.executeUpdate();
            messageLabel.setForeground(Color.green);
            messageLabel.setText("Your gmail updated successfully .");
            gmailField.setText("");
            oldPasswordField.setText("");
            return;
            
        }catch(SQLException e){
            messageLabel.setText("Failed in connection to the database .");
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

